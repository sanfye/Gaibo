package com.gaibo.biz.share.vo;

public class WarningFromDb {
	private String id;
	private String warningId;
	private String warningEnglish;
	private String warningChinese;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getWarningId() {
		return warningId;
	}
	public void setWarningId(String warningId) {
		this.warningId = warningId;
	}
	public String getWarningEnglish() {
		return warningEnglish;
	}
	public void setWarningEnglish(String warningEnglish) {
		this.warningEnglish = warningEnglish;
	}
	public String getWarningChinese() {
		return warningChinese;
	}
	public void setWarningChinese(String warningChinese) {
		this.warningChinese = warningChinese;
	}
	
	
}
